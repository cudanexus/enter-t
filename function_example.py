# Function Examples

def welcom(name):
    """Simple greeting function"""
    return f"Hello, {name}!"

def addition(a, b):
    """Add two numbers"""
    return a + b

def multi(x, y):
    """Multiply two numbers"""
    return x * y

# Example usage
if __name__ == "__main__":
    print(welcom("mustafa"))
    print(f"5 + 3 = {addition(5, 3)}")
    print(f"4 * 6 = {multi(4, 6)}")
