# Function Examples

def greet(name):
    """Simple greeting function"""
    return f"Hello, {name}!"

def add(a, b):
    """Add two numbers"""
    return a + b

def multiply(x, y):
    """Multiply two numbers"""
    return x * y

# Example usage
if __name__ == "__main__":
    print(greet("mustafa"))
    print(f"5 + 3 = {add(5, 3)}")
    print(f"4 * 6 = {multiply(4, 6)}")
